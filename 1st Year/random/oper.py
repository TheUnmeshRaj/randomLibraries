# Initial list
Example = ['E', 'D', 'C', 'B', 'A']

# Sorting the list
Example.sort()
print("Sorted List:", Example)

# Extending the list
new_elements = ['F', 'G']
Example.extend(new_elements)
print("Extended List:", Example)

# Finding index of an element
index_of_C = Example.index('C')
print("Index of 'C':", index_of_C)

# Copying the list
copied_list = Example
print("Copied List:", copied_list)
