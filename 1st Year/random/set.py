E = {1, 2, 3, 4, 5}
F = {2, 4, 6, 8, 10}
# Union Operation
print(f"The Union operation returns: {E|F}")
# Intersection Operation
print(f"The Intersection operation returns: {E&F}")
# Set Difference Operation
print(f"The Difference operation returns: {F-E}")
# Symmetric Difference Operation
print(f"The Union operation returns: {E^F}")
